func bitmask(numbits uint64) uint64 {
	if numbits == 64 {
		return math.MaxUint64
	}
	return uint64(1)<<numbits - 1
}

func bitrank(val, pos uint64) uint64 {
	val = val & ((2 << pos) - 1)
	return popcount.Count64(val)
}

func popcntv(val uint64, ignore uint64) uint64 {
	var res uint64
	if ignore%64 != 0 {
		res = popcnt(val & ^bitmask(ignore%64))
	} else {
		res = popcnt(val)
	}
	return res
}

func popcnt(val uint64) uint64 {
	return popcount.Count64(val)
}

func shiftIntoB(a, b, bstart, bend, amount uint64) uint64 {
	var a_component uint64
	if bstart == 0 {
		a_component = a >> (64 - amount)
	}
	b_shifted_mask := bitmask(bend-bstart) << bstart
	b_shifted := ((b_shifted_mask & b) << amount) & b_shifted_mask
	b_mask := ^b_shifted_mask
	return a_component | b_shifted | (b & b_mask)
}

func bitselectv(val, ignore, rank uint64) uint64 {
	valNew := val & ^bitmask(ignore%64)
	return bitselect(valNew, rank)
}

func bitselect(x uint64, k uint64) uint64 {
	if k >= popcnt(x) {
		return 64
	}

	kOnesStep4 := uint64(0x1111111111111111)
	kOnesStep8 := uint64(0x0101010101010101)
	kMSBsStep8 := uint64(0x80 * kOnesStep8)

	s := x
	s = s - ((s & (uint64(0xA) * kOnesStep4)) >> 1)
	s = (s & (0x3 * kOnesStep4)) + ((s >> 2) & (0x3 * kOnesStep4))
	s = (s + (s >> 4)) & (0xF * kOnesStep8)
	byteSums := s * kOnesStep8

	kStep8 := k * kOnesStep8
	geqKStep8 := (((kStep8 | kMSBsStep8) - byteSums) & kMSBsStep8)
	place := popcnt(geqKStep8) * 8
	byteRank := k - (((byteSums << 8) >> place) & (uint64((0xFF))))

	pos := ((x >> place) & 0xFF) | (byteRank << 8)
	return place + selectTable[pos]
}

